// We can know the 'type' of variable by coding too.


// Number

var price = 20;

console.log(price); // output: 20

console.log(typeof price); // output: number [To know the 'type' of this variable]



// String

var name = 'Abrar Fahad';

console.log(name); // output: Abrar Fahad

console.log(typeof name); // output: string



// Boolean

var pass = true;

console.log(pass); // output: true

console.log(typeof pass); // output: boolean


// undefined

var haveTime;

console.log(haveTime); // output: undefined [Here we don't say the value of this variable]



// object

var today = new Date(); // [It is one type of Object Data]

console.log(today); // output: 2022-01-28T15:54:58.757Z

console.log(typeof today); // output: object



