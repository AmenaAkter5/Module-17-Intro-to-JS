var onionPrice = 43;
var eggPrice = 42;

// To see the output of these variables: [run the program on console]

console.log(onionPrice); // output: 43
console.log(eggPrice); // output: 42


// To make addition of these variables: [run the program on console]


console.log(onionPrice + eggPrice); // output: 85



// The output of addition of these two variables We can declare it in one variable


var totalPrice = onionPrice + eggPrice;

// to see the output of this variable: [run the program on console]

console.log(totalPrice); // output: 85



// The output of subtraction of these two variables We can declare it in one variable


var priceDifference = onionPrice - eggPrice;


// to see the output of this variable: [run the program on console]


console.log(priceDifference); // output: 1



// Multiplication: We have eggPrice, we need eggQuantity.

eggPrice = 42; // [Previously Declared variable so we don't write var before to say the value of this variable]

var eggQuantity = 5;

//    variable declaration for multiplication

var totalEggPrice = eggPrice * eggQuantity;


// To see the output:


console.log(totalEggPrice); // output: 210




// Divide:

var moneyAvailable = 100;
var orangePrice = 5;


//    variable declaration for Divide

var orangeQuantity = (moneyAvailable / orangePrice);


// To see the output:

console.log(orangeQuantity); // output: 20



